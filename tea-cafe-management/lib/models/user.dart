class MyUser {
  final String uid;
  final String email;
  MyUser({required this.uid, required this.email});
}

class MyUserData {
  final String uid;
  final String name;
  final String sugar;
  final int strength;
  MyUserData({
    required this.uid,
    required this.name,
    required this.sugar,
    required this.strength,
  });
}
